-- Jeep Wrangler Trail Hunt official card audit.
-- Jeep Korea exposes Trail Hunt as a Wrangler edition/card, so it is kept as a
-- Wrangler trim/powertrain candidate instead of a standalone vehicle_model.
-- Row-level domestic specifications are not verified yet; rows stay pending,
-- non-selectable, and free of numeric specs.

