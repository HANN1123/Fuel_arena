  id,
  model_year_id,
  generation_id,
  trim_name,
  engine_name,
  fuel_type,
  efficiency_unit,
  vehicle_class,
  fuel_league,
  source_name,
  source_url,
  confidence_score,
  sort_order
) as (
  values
    (
      'variant-jeep-152-kr-2026-gasoline',
      'year-jeep-152-kr-2026',
      'generation-jeep-wrangler-official-lineup',
      'Wrangler',
      'Pending official Wrangler specification review',
      '가솔린',
      'km/L',
      'SUV',
      'gasoline',
      'Jeep Korea official Wrangler page',
      'https://www.jeep.co.kr/wrangler.html',
      0.58,
      10
    ),
    (
      'variant-jeep-wrangler-2026-trail-hunt-pending',
      'year-jeep-152-kr-2026',
      'generation-jeep-wrangler-official-lineup',
      'Wrangler Trail Hunt Edition',
      'Pending official Wrangler Trail Hunt Edition specification review',
      '가솔린',
      'km/L',
      'SUV',
      'gasoline',
      'Jeep Korea official Wrangler Trail Hunt Edition page',
      'https://www.jeep.co.kr/JL/wrangler/edition.html',
